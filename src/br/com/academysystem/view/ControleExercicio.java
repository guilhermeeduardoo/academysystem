    public void readDadostabela() {
        DefaultTableModel modeloTabela = (DefaultTableModel) tabelaexercicio.getModel();
        ExercicioDAO dao = new ExercicioDAO();
        modeloTabela.setNumRows(0);
        dao.listarExercicio().stream().forEach((Exercicio exercicio) -> {
            modeloTabela.addRow(new Object[]{
                exercicio.getId(),
                exercicio.getNomeExercicio(),
                exercicio.getCategoriaExercicio()
                     });
        });
    }

