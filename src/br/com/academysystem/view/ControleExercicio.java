    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        Exercicio exercicio = new Exercicio();
        ExercicioDAO dao = new ExercicioDAO();
        exercicio.setId(Integer.parseInt(id.getText()));
        exercicio.setNomeExercicio(nomeexercicio.getText());
        exercicio.setCategoriaExercicio(categoria.getText());
        dao.alterarCliente(exercicio);
        readDadostabela();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        if (tabelaexercicio.getSelectedRow() != -1) {
            Exercicio exercicio = new Exercicio();
            ExercicioDAO dao = new ExercicioDAO();
            exercicio.setId(Integer.parseInt(id.getText()));
            dao.excluirExercicio(exercicio);
        }
        readDadostabela();
    }//GEN-LAST:event_jButton7ActionPerformed

    private void tabelaexercicioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaexercicioMouseClicked
        // TODO add your handling code here:
        if (tabelaexercicio.getSelectedRow() != -1) {
            id.setText(tabelaexercicio.getValueAt(tabelaexercicio.getSelectedRow(), 0).toString());
            nomeexercicio.setText(tabelaexercicio.getValueAt(tabelaexercicio.getSelectedRow(), 1).toString());
            categoria.setText(tabelaexercicio.getValueAt(tabelaexercicio.getSelectedRow(), 2).toString());
        }
    }//GEN-LAST:event_tabelaexercicioMouseClicked


